T = ['甲','已','丙','丁','戊','己','庚','辛','壬','癸']
D = ['子','丑','寅','卯','辰','巳','午','未','申','酉','戌','亥']
while True:
    print("輸入0退出查詢，輸入年份查詢天干地支紀年") 
    year = int(input('請輸入: '))
    
    if year == 0:
        print("查詢結束！")
        break
    
    N = (year-3)%60
    t = N%10
    d = N%12 
    print('公元 {0} 年是天干地支的 {1}{2} 年\n'.format(year,T[t-1],D[d-1]))
